import SwiftUI

enum AppColor {

    // MARK: - Brand
    static let primary = Color(hex: "#1A73E8")
    static let secondary = Color(hex: "#34A853")
    static let accent = Color(hex: "#FBBC05")

    // MARK: - Background
    static let background = Color(hex: "#FFFFFF")
    static let surface = Color(hex: "#F5F5F5")

    // MARK: - Text
    static let textPrimary = Color(hex: "#1F1F1F")
    static let textSecondary = Color(hex: "#6B7280")
    static let textDisabled = Color(hex: "#9CA3AF")

    // MARK: - Status
    static let success = Color(hex: "#22C55E")
    static let warning = Color(hex: "#F59E0B")
    static let error = Color(hex: "#EF4444")

    // MARK: - Border
    static let border = Color(hex: "#E5E7EB")
    static let divider = Color(hex: "#D1D5DB")
}
